  // So sánh 2 mảng có giống nhau
  function areArraysEqual(arr1, arr2) {
    // Kiểm tra xem hai mảng có cùng độ dài hay không
    if (arr2 !== undefined && arr2.length === 0) {
      return null
    }
    if (arr1?.length !== arr2?.length) {
      return false;
    }

    // Lặp qua các phần tử của mảng thứ nhất (arr1)
    for (let i = 0; i < arr1?.length; i++) {
      const value1 = arr1[i]?.value;

      // Tìm phần tử trong mảng thứ hai (arr2) có giá trị value giống với value1
      const foundItem = arr2.find(item => item?.value === value1);

      // Nếu không tìm thấy phần tử, tức là giá trị không giống nhau, trả về false
      if (!foundItem) {
        return false;
      }
    }

    // Nếu qua được tất cả các kiểm tra, tức là giá trị giống nhau, trả về true
    return true;
  }



