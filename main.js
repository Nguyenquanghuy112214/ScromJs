  function TextToSpeech(intermediary, value, IsMale) {
    intermediary.style.cursor = "pointer";
    if ("speechSynthesis" in window) {
      // Tạo một instance của SpeechSynthesisUtterance
      var utterance = new SpeechSynthesisUtterance();

      // Thiết lập văn bản cần chuyển đổi thành giọng nói
      utterance.text = value;

      // Chọn ngôn ngữ và giọng đọc (nếu có)
      utterance.lang = "en-US";
      // Lấy danh sách giọng đọc
      var voices = speechSynthesis.getVoices();

      // Tìm giọng đọc nam
      var maleVoice = voices.find(function (voice) {
        return voice.name === "Microsoft David - English (United States)";
      });

      // Tìm giọng đọc nữ
      var femaleVoice = voices.find(function (voice) {
        return voice.name === "Microsoft Zira - English (United States)";
      });

      if (IsMale) {
        utterance.voice = maleVoice; // Chọn giọng nam
      } else {
        // Thiết lập giọng nam hoặc nữ
        utterance.voice = femaleVoice; // Chọn giọng nữ
      }

      // Bắt đầu chuyển đổi văn bản thành giọng nói
      intermediary.addEventListener("click", () =>
        speechSynthesis.speak(utterance)
      );
    }
  }

